def test_stream_default_layout() -> None:
    chunks = Hdf5Reader(TEST_DATA).stream().to_chunks()

    assert {c.entity_path for c in chunks} == {
        "/",
        "/observations",
        "/observations/images",
        "/meta",
        "/__hdf5_properties",
        "/__hdf5_properties/observations",
        "/__hdf5_properties/observations/qpos",
    }

    # Multi-dataset groups pack into a single `data` struct component.
    root = _by_entity(chunks, "/")
    assert root.num_rows == 5
    root_struct = root.to_record_batch().schema.field("data").type.value_type
    assert root_struct.names == ["labels", "time"]

    observations = _by_entity(chunks, "/observations")
    assert observations.num_rows == 5
    observations_struct = observations.to_record_batch().schema.field("data").type.value_type
    assert observations_struct.names == ["qpos", "qvel"]
    # 2-D [5, 3] → FixedSizeList<3> struct field; 1-D → scalar field.
    assert observations_struct.field("qpos").type == pa.list_(pa.float64(), 3)
    assert observations_struct.field("qvel").type == pa.float32()

    # A single-dataset group emits a bare component, not a one-field struct.
    images = _by_entity(chunks, "/observations/images")
    rb = images.to_record_batch()
    assert "data" not in rb.schema.names
    # 4-D [5, 2, 2, 3] → one 12-element row-major blob per row.
    cam0 = rb.column("cam0").to_pylist()
    assert len(cam0) == 5
    assert cam0[0] == [list(range(12))]

    # 0-D datasets are static.
    meta = _by_entity(chunks, "/meta")
    assert meta.is_static
    assert meta.num_rows == 1
    assert meta.to_record_batch().column("count").to_pylist() == [[42]]


def test_stream_attribute_chunks() -> None:
    chunks = Hdf5Reader(TEST_DATA).stream().to_chunks()

    root_props = _by_entity(chunks, "/__hdf5_properties")
    assert root_props.is_static
    rb = root_props.to_record_batch()
    assert rb.column("description").to_pylist() == [["canonical re_hdf5 test fixture"]]
    assert rb.column("version").to_pylist() == [[1]]

    observation_props = _by_entity(chunks, "/__hdf5_properties/observations")
    rb = observation_props.to_record_batch()
    assert rb.column("frequency").to_pylist() == [[30.0]]
    # A float64[3] attribute maps to one fixed-size list row.
    assert rb.schema.field("joints").type.value_type == pa.list_(pa.float64(), 3)
    assert rb.column("joints").to_pylist() == [[[1.0, 2.0, 3.0]]]

    qpos_props = _by_entity(chunks, "/__hdf5_properties/observations/qpos")
    assert qpos_props.to_record_batch().column("unit").to_pylist() == [["rad"]]


def test_use_structs_false() -> None:
    chunks = Hdf5Reader(TEST_DATA).stream(use_structs=False).to_chunks()

    observations = _by_entity(chunks, "/observations")
    rb = observations.to_record_batch()
    assert "data" not in rb.schema.names
    assert rb.column("qvel").to_pylist() == [[0.0], [1.0], [2.0], [3.0], [4.0]]
    assert rb.column("qpos").to_pylist()[0] == [[0.0, 0.1, 0.2]]


def test_entity_path_prefix() -> None:
    chunks = Hdf5Reader(TEST_DATA).stream(entity_path_prefix="/world").to_chunks()
    entities = {c.entity_path for c in chunks}
    assert "/world/observations" in entities
    assert "/world/__hdf5_properties" in entities
    assert all(e.startswith("/world") for e in entities)


def test_index_column() -> None:
    chunks = Hdf5Reader(TEST_DATA).stream(index_column=IndexColumn.timestamp("/time", input_unit="s")).to_chunks()

    # The index is consumed: the root group is left with `labels` only, which
    # emits as a bare component.
    root = _by_entity(chunks, "/")
    rb = root.to_record_batch()
    assert "data" not in rb.schema.names
    assert rb.column("labels").to_pylist() == [["idle"], ["reach"], ["grasp"], ["lift"], ["place"]]

    # The timeline is named after the index dataset's leaf, and float seconds
    # scale to nanoseconds without losing sub-second precision.
    assert rb.schema.field("time").type == pa.timestamp("ns")
    assert rb.column("time").cast(pa.int64()).to_pylist() == [
        0,
        500_000_000,
        1_000_000_000,
        1_500_000_000,
        2_000_000_000,
    ]

    # The whole file shares the timeline.
    observations = _by_entity(chunks, "/observations")
    assert observations.to_record_batch().schema.field("time").type == pa.timestamp("ns")


def test_row_index_timeline_by_default() -> None:
    chunks = Hdf5Reader(TEST_DATA).stream().to_chunks()
    root = _by_entity(chunks, "/")
    rb = root.to_record_batch()
    assert rb.schema.field("row_index").type == pa.int64()
    assert rb.column("row_index").to_pylist() == [0, 1, 2, 3, 4]


def test_ignore_datasets() -> None:
    # Ignoring a group path excludes the whole subtree.
    chunks = Hdf5Reader(TEST_DATA).stream(ignore_datasets=["/observations/images"]).to_chunks()
    assert not any(c.entity_path == "/observations/images" for c in chunks)

    # Ignoring a single dataset flattens its group to the remaining dataset.
    chunks = Hdf5Reader(TEST_DATA).stream(ignore_datasets=["/observations/qvel"]).to_chunks()
    observations = _by_entity(chunks, "/observations")
    rb = observations.to_record_batch()
    assert "data" not in rb.schema.names
    assert "qpos" in rb.schema.names


