"""
Tests for rerun.experimental.Hdf5Reader.

Wrapper-plumbing tests against the committed canonical fixture
(`tests/assets/hdf5/`, generated by `cargo run -p re_hdf5 --example
gen_test_fixture`). The mapping logic itself is covered by `re_hdf5`'s Rust
integration tests.

Fixture layout (5 rows): `/time` (float64 seconds), `/labels` (strings),
`/observations/{qpos [5,3], qvel [5]}`, `/observations/images/cam0 [5,2,2,3]`,
a 0-D `/meta/count`, and attributes on the root, `/observations`, and
`/observations/qpos`.
"""

from __future__ import annotations

from pathlib import Path

import pyarrow as pa
import pytest
from rerun.experimental import Chunk, DatasetInfo, Hdf5Reader, IndexColumn

HDF5_ASSETS_DIR = Path(__file__).resolve().parents[1] / "assets" / "hdf5"
TEST_DATA = HDF5_ASSETS_DIR / "test_data.h5"
TEST_DATA_MISALIGNED = HDF5_ASSETS_DIR / "test_data_misaligned.h5"


def _by_entity(chunks: list[Chunk], entity_path: str) -> Chunk:
    matches = [c for c in chunks if c.entity_path == entity_path]
    assert len(matches) == 1, f"expected exactly one chunk at {entity_path}, found {len(matches)}"
    return matches[0]


