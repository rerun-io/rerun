def test_file_not_found(tmp_path: Path) -> None:
    with pytest.raises(FileNotFoundError, match="not found"):
        Hdf5Reader(tmp_path / "nonexistent.h5")


def test_misaligned_datasets_raise_value_error() -> None:
    # Eager validation: the error surfaces at `stream()`, before iteration.
    with pytest.raises(ValueError, match="not row-aligned"):
        Hdf5Reader(TEST_DATA_MISALIGNED).stream()

    # Explicitly ignoring the offender resolves the mismatch.
    chunks = Hdf5Reader(TEST_DATA_MISALIGNED).stream(ignore_datasets=["/b"]).to_chunks()
    assert _by_entity(chunks, "/").num_rows == 4


def test_bad_index_column_raises_value_error() -> None:
    reader = Hdf5Reader(TEST_DATA)

    # (The timeline kind and unit are typed via `IndexColumn`, so a bad kind/unit
    # is a type error rather than a runtime one; only path/shape issues reach here.)
    with pytest.raises(ValueError, match="not found"):
        reader.stream(index_column=IndexColumn.sequence("/missing"))

    with pytest.raises(ValueError, match="1-dimensional"):
        reader.stream(index_column=IndexColumn.sequence("/observations/qpos"))


def test_missing_attribute_path_raises_key_error() -> None:
    with pytest.raises(KeyError, match="not found"):
        Hdf5Reader(TEST_DATA).attributes("/missing")

    with pytest.raises(KeyError, match="not found"):
        Hdf5Reader(TEST_DATA).groups("/missing")
