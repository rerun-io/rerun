def test_groups() -> None:
    reader = Hdf5Reader(TEST_DATA)
    assert reader.groups() == ["/meta", "/observations", "/observations/images"]
    assert reader.groups("/observations") == ["/observations/images"]
    assert reader.groups("/observations/images") == []


def test_datasets() -> None:
    reader = Hdf5Reader(TEST_DATA)
    infos = reader.datasets()
    assert infos == [
        DatasetInfo(path="/labels", shape=(5,), dtype="string"),
        DatasetInfo(path="/time", shape=(5,), dtype="float64"),
        DatasetInfo(path="/meta/count", shape=(), dtype="int64"),
        DatasetInfo(path="/observations/qpos", shape=(5, 3), dtype="float64"),
        DatasetInfo(path="/observations/qvel", shape=(5,), dtype="float32"),
        DatasetInfo(path="/observations/images/cam0", shape=(5, 2, 2, 3), dtype="uint8"),
    ]

    under_group = reader.datasets("/observations/images")
    assert under_group == [DatasetInfo(path="/observations/images/cam0", shape=(5, 2, 2, 3), dtype="uint8")]


def test_attributes() -> None:
    reader = Hdf5Reader(TEST_DATA)

    assert reader.attributes() == {
        "description": "canonical re_hdf5 test fixture",
        "version": 1,
    }
    # Attribute order is not guaranteed by HDF5 — compare as a dict/set.
    assert reader.attributes("/observations") == {
        "frequency": 30.0,
        "joints": [1.0, 2.0, 3.0],
    }
    assert reader.attributes("/observations/qpos") == {"unit": "rad"}
    assert reader.attributes("/meta") == {}


def test_path_property_and_repr() -> None:
    reader = Hdf5Reader(TEST_DATA)
    assert reader.path == TEST_DATA
    assert repr(reader) == f"Hdf5Reader({TEST_DATA})"


