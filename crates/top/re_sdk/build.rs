use std::fmt::Write as _;
use std::fs;
use std::path::{Path, PathBuf};

#[derive(Debug)]
struct ViewDef {
    name: String,
    identifier: String,
    fields: Vec<ViewField>,
}

#[derive(Debug)]
struct ViewField {
    name: String,
    archetype: String,
}

fn main() {
    re_build_tools::export_build_info_vars_for_crate("re_sdk");
    generate_blueprint_views();
}

fn generate_blueprint_views() {
    let manifest_dir = PathBuf::from(std::env::var("CARGO_MANIFEST_DIR").unwrap());
    let view_def_dir =
        manifest_dir.join("../../store/re_sdk_types/definitions/rerun/blueprint/views");

    if !view_def_dir.exists() {
        return;
    }

    let view_def_dir = view_def_dir.canonicalize().unwrap();

    println!("cargo:rerun-if-changed={}", view_def_dir.display());

    let mut views = fs::read_dir(&view_def_dir)
        .unwrap()
        .map(|entry| entry.unwrap().path())
        .filter(|path| path.extension().is_some_and(|ext| ext == "fbs"))
        .map(|path| parse_view_definition(&path))
        .collect::<Vec<_>>();

    views.sort_by(|lhs, rhs| lhs.name.cmp(&rhs.name));

    let generated_path = manifest_dir.join("src/blueprint/generated_views.rs");
    println!("cargo:rerun-if-changed={}", generated_path.display());

    let expected = re_build_tools::rustfmt_str(&code_for_views(&views)).unwrap();
    let actual = fs::read_to_string(&generated_path).unwrap();
    if expected != actual {
        panic!(
            "{} is out of date; regenerate it from the blueprint view definitions",
            generated_path.display()
        );
    }
}

fn parse_view_definition(path: &Path) -> ViewDef {
    println!("cargo:rerun-if-changed={}", path.display());

    let source = fs::read_to_string(path).unwrap();
    let table_start = source.find("table ").unwrap();
    let after_table = &source[table_start + "table ".len()..];
    let name = after_table
        .split_once(|c: char| c.is_whitespace() || c == '(')
        .unwrap()
        .0
        .to_owned();

    let identifier_attr = "\"attr.rerun.view_identifier\":";
    let identifier_start = source.find(identifier_attr).unwrap() + identifier_attr.len();
    let identifier_source = source[identifier_start..].trim_start();
    let identifier = identifier_source
        .strip_prefix('"')
        .unwrap()
        .split_once('"')
        .unwrap()
        .0
        .to_owned();

    let body_start = source.find('{').unwrap() + 1;
    let body_end = source.rfind('}').unwrap();
    let mut fields = Vec::new();

    for line in source[body_start..body_end].lines() {
        let line = line.trim();
        if line.is_empty() || line.starts_with("///") || line.starts_with("//") {
            continue;
        }

        let Some((field_name, rest)) = line.split_once(':') else {
            continue;
        };
        let Some(type_name) = rest.split_whitespace().next() else {
            continue;
        };

        fields.push(ViewField {
            name: field_name.trim().to_owned(),
            archetype: type_name.rsplit('.').next().unwrap().to_owned(),
        });
    }

    ViewDef {
        name,
        identifier,
        fields,
    }
}

fn code_for_views(views: &[ViewDef]) -> String {
    let mut code = String::new();
    code.push_str("// DO NOT EDIT! This file was auto-generated by crates/top/re_sdk/build.rs\n\n");

    let unique_archetypes = views
        .iter()
        .flat_map(|view| &view.fields)
        .map(|field| field.archetype.as_str())
        .collect::<std::collections::BTreeSet<_>>();

    for archetype in unique_archetypes {
        writeln!(
            code,
            "impl crate::blueprint::IntoViewProperty<re_sdk_types::blueprint::archetypes::{archetype}> for re_sdk_types::blueprint::archetypes::{archetype} {{"
        )
        .ok();
        writeln!(
            code,
            "    fn into_view_property(self) -> re_sdk_types::blueprint::archetypes::{archetype} {{ self }}"
        )
        .ok();
        code.push_str("}\n\n");
        writeln!(
            code,
            "impl crate::blueprint::IntoViewProperty<re_sdk_types::blueprint::archetypes::{archetype}> for &re_sdk_types::blueprint::archetypes::{archetype} {{"
        )
        .ok();
        writeln!(
            code,
            "    fn into_view_property(self) -> re_sdk_types::blueprint::archetypes::{archetype} {{ self.clone() }}"
        )
        .ok();
        code.push_str("}\n\n");
    }

    for view in views {
        writeln!(code, "/// Blueprint wrapper for `{}`.", view.name).ok();
        writeln!(code, "pub struct {}(pub(crate) View);\n", view.name).ok();

        writeln!(code, "impl {} {{", view.name).ok();
        writeln!(code, "    /// Create a new `{}`.", view.name).ok();
        code.push_str("    pub fn new(name: impl Into<String>) -> Self {\n");
        writeln!(
            code,
            "        Self(View::new(\"{}\", Some(name.into())))",
            view.identifier
        )
        .ok();
        code.push_str("    }\n\n");
        code.push_str("    impl_view_common_methods!();\n\n");

        for field in &view.fields {
            writeln!(code, "    /// Configure `{}`.", field.name).ok();
            writeln!(code, "    pub fn with_{}(", field.name).ok();
            code.push_str("        mut self,\n");
            writeln!(
                code,
                "        {}: impl crate::blueprint::IntoViewProperty<re_sdk_types::blueprint::archetypes::{}>,",
                field.name, field.archetype
            )
            .ok();
            code.push_str("    ) -> Self {\n");
            writeln!(
                code,
                "        self.0.add_property(\"{}\", &{}.into_view_property());",
                field.archetype, field.name
            )
            .ok();
            code.push_str("        self\n");
            code.push_str("    }\n\n");
        }

        code.push_str("}\n\n");
        writeln!(
            code,
            "impl From<{}> for crate::blueprint::ContainerLike {{",
            view.name
        )
        .ok();
        writeln!(code, "    fn from(view: {}) -> Self {{", view.name).ok();
        code.push_str("        Self::View(view.0)\n");
        code.push_str("    }\n");
        code.push_str("}\n\n");
    }

    code
}
